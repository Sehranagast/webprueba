                       <h1 class="text-center">Anduin Lothar</h1><br><br>
                      
                       <p>Sir Anduin Lothar, el León de Azeroth, fue el último descendiente del linaje real de los Arathi,
                        un caballero campeón del Reino de Azeroth durante la Primera Guerra, y el comandante supremo de las fuerzas de la Alianza de Lordaeron durante
                         la Segunda Guerra. Pereció en combate en la base de Montaña Roca Negra en manos del Jefe de Guerra orco Orgrim Doomhammer justo antes del fin de la guerra. Lothar 
                         es la figura más prominente de la raza humana en la historia de 
                         Warcraft y el principal responsable de la creación de la Alianza. El Rey Varian Wrynn llamó a su hijo en honor de uno de los personajes humanos más prominentes.
              </p>
              <hr>
              <h2 class="text-center">Antes de la guerra</h2>
              <p>Se crió en la corte del Rey Adamant Wrynn III, al lado de sus dos mejores amigos, el príncipe Llane Wrynn, heredero del trono, y Medivh, el hijo del conjurador del Rey
               A los 12 años, su Edad de la Ascensión, ingresa en el Regimiento de Caballería de Azeroth, conocida como “La Gran Hermandad de los Caballos”, y a partir de allí, los hechos
                más importantes de su vida serán narrados en su diario, el cual nos ha sido legado solamente en fragmentos, conocidos actualmente como “Crónicas de la Guerra en Azeroth”.

Llegaría a convertirse en uno de los principales de la corte: Comandante en jefe de la Hermandad y encargado de la seguridad del reino, es nombrado “Sir”.
 Lothar toma protagonismo en la vida de Azeroth a partir del año 583, cuando a los 36 años, se hará cargo de la defensa de Ventormenta ante el repentino ataque de “unas criaturas monstruosas 
 de piel verde”. Un año después, tras la muerte de Wrynn II, Llane es erigido como Rey de Azeroth, a los 20 años, y Lothar es el encargado de dirigir sus ejércitos en cientos de escaramuzas contra
  los invasores, llamados orcos, en los territorios circundantes al Bosque de Elwynn, principalmente la Ciénaga Negra y el Pantano de las Lamentaciones.
  <h2 class="text-center">Primera Guerra</h2>
      <p>Cuando los orcos llegaron por primera vez a Azeroth y lanzaron el primer ataque al castillo de Ventormenta, Lothar defendió la ciudad contra tal agresividad.
       El rey Adamant Wrynn III lo apoyó y se comprometió a liberar su tierra de los hostiles orcos. Sin embargo, murió, antes de poder cumplir su promesa y su hijo Llane Wrynn I subió al trono.
        El nuevo rey continuó el trabajo de su padre y las cruentas batallas contra los orcos empezaron. Durante este tiempo, los invasores fueron contenidos en el Pantano de las Penas.

En cierto punto de la guerra, el Escrito sobre divinidad, un libro de gran valor para los clérigos de Villanorte, fue robado por una banda de ogros liderados por su señor, Turok.
 Lothar lideró a una expedición hacia la escondite de los orcos, las Minas de la Muerte en lo que hoy es Páramos de Poniente, pero fueron sorprendidos y tomados cautivos, siendo varios de ellos asesinados lentamente.
  Lothar estuvo preso en las minas por 20 meses hasta que los pocos supervivientes que quedaron fueron rescatados por los ejércitos de Azeroth. Lograron recuperar el sagrado libro y regresaron a Ventormenta, guardando el libro en la Abadía de Villanorte.
   Volviendo a combatir en la guerra, Lothar tomó el mando de los ejércitos de Azeroth.

Lothar quedó conmocionado cuando el hechicero Khadgar, aprendiz de su viejo amigo Medivh, llegó diciéndoles que el gran mago había traicionado a la humanidad al abrir el portal que perimitó el acceso de los orcos a Azeroth.
 Después de una polémica discusión en Ventormenta, Lothar dirigió personalmente unas tropas para atacar Karazhan y acabar con la vida de Medivh. El ejército, junto a Lothar, Khadgar y la semi-orca Garona, descendieron a la torre y se enfrentaron al guardián enloquecido.
  Khadgar finalmente logró apuñalar a Medivh en el corazón, y Anduin le cortó la cabeza tras una potente estocada proveniente de su gran espada.

Desafortunadamente, no pudo detener el creciente avance de la Horda. La guerra se venía contra Azeroth y los orcos aprendían de sus errores bajo el liderazfo del Jefe de Guerra Blackhand. Poco después, el Rey Llane fue asesinado por la espía Garona y la ciudad de Ventormenta fue asaltada y saqueada.
 Lothar, al ver que el Reino Azeroth se estaba perdiendo, decidió salvar lo que quedaba de la, hasta entonces, poderosa nación. Reunió a los soldados supervientes y a todos los civiles que pudo rescatar y desesperadamente los llevó cruzando el Gran Mar, llegando a las costas de Lordaeron.
              </p>
              <h2 class="text-center">Segunda Guerra</h2>
              <p>Una vez en la corte del rey Terenas Menethil II de Lordaeron, Lothar contó la terrible historia a todos los que lo escuchaban. El elocuente discurso de Lothar llevó a los
               jefes de los reinos humanos a una reunión de emergencia, donde la política magistral de Terenas llevó a crear a la poderosa Alianza de Lordaeron. Después de haber sido
                contactados por Terenas con el relato de Lothar, los lejanos elfos nobles enviaron a sus fuerzas, con el fin de poder pagar la deuda que tenían con el linaje real de
                 los Arathi por salvarlos durante las Guerras Trol. Los enanos Martillo Salvaje de Pico Nidal también habían sido atacados por la Horda y rápidamente se unieron a Lothar y a la Alianza
                 . Los refuerzos no dejarían de llegar ya que los enanos Barbabronce y los gnomos de Khaz Modan se unieron al verse obligados a dejar sus tierras por el avance de la Horda.

Debido a su gran habilidad en la guerra, su vasta experiencia con los orcos y a que los reyes del norte no se sentían cómodos poniendo sus tropas bajo el símbolo de una nación rival, Lothar,
 que pertenecía a un reino ya perdido, fue nombrado supremo comandante de las fuerzas de la Alianza. El nuevo comandante nombró a Turalyon como segundo al mando, Almirante a Daelin Proudmoore
 , y a Uther el Iluminado y al Archimago Khadgar como lugartenientes. A lo largo de la batalla, dirigió sus fuerzas guerra tras guerra con gran habilidad y valor. Después de la misteriosa
  retirada de la Horda de Lordaeron y las victoria del Almirante Proudmoore en el mar, Lothar llevó a los ejércitos de la Alianza para recuperar gran parte de Azeroth y Khaz Modan
   (con la ayuda de Muradin y Brann Barbabronce), abriéndose paso por la Ciénaga Negra. Lothar intentó parlamentar con el nuevo jefe orco para arreglar un armisticio.

Habiéndose separado del ejército, éste corrió en ayuda de su comandante, lo que originó una de las batallas más grandes de la historia de Azeroth. En medio del caos, fue forzado a combatir con Orgrim Doomhammer
, Jefe de Guerra de la Horda. Después de una larga batalla, Lothar cayó muerto. Se dice que Doomhammmer no "jugó" limpio y que Lothar fue asesinado al ser atacado por un grupo de guerreros de la Horda en medio del duelo con el Jefe de la Guerra.

Orgrim pensó que la muerte de Lothar rompería la moral de sus tropas, sin embargo, sucedió lo contrario, el mismo Turalyon cogió la espada de Lothar y llevó a la Alianza a la victoria, avanzando lo suficiente para que Khadgar cerrase el Portal Oscuro.</p>
<h2 class="text-center">Legado</h2>
<p>
Anduin no logró contemplar cómo sus amadas tierras quedaron libres del control de los orcos y fueron reconstruidas. En honor al gran Lothar, en medio de las Estepas Ardientes, se erigió un monumento
 construido por los prisioneros orcos y apuntando con la espada a modo de reto a la montaña Roca Negra. El legado de Lord Lothar vive en todas las personas libres de Ventormenta.
  El actual príncipe de la nación lleva el nombre de "Anduin", en honor a Lothar.
La posterior Expedición de la Alianza a Draenor, a través del portal, al mando de varios veteranos que lucharon junto a Lothar, se llamó a sí misma como Hijos de Lothar "en honor al hombre más grande y abnegado que alguna vez existió"
, como dice Khadgar. El Monumento para el recuerdo en el Bastión del honor fue erigido como recuerdo por todo lo que luchó.
Un cinturón que alguna vez perteneció a Lothar llegó a manos del Anillo Carmesí, un grupo de gladiadores de Orgrimmar, y fue guardado en un salón con otras legendarias armas y armaduras conocida como la Sala de las Leyendas.
 El cinturón fue reclamado por Varian Wrynn, en la época en que fué capturado por los orcos y obligado a ser gladiador, bajo el nombre de Lo'Gosh, cuando le fue permitido acceder a la sala.</p>
